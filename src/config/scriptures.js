// Function to get scripture for a specific date
export const getScriptureByDate = (dateString) => {
    return scriptures.find(scripture => scripture.date === dateString) || null;
};

// Function to get scripture for today
export const getTodayScripture = () => {
    const today = new Date();
    const formattedDate = today.toISOString().split('T')[0]; // Format: YYYY-MM-DD
    return getScriptureByDate(formattedDate) || scriptures[0]; // Fallback to first entry if no match
};

// Function to get all scriptures
export const getAllScriptures = () => {
    return scriptures;
};

export default {
    getScriptureByDate,
    getTodayScripture,
    getAllScriptures
};